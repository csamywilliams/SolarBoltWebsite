Solar PV systems convert the sun's rays into electrical power using photovoltaic cells. 
They do not require direct sunlight to generate as they can still work in cloudy conditions.
The electricity that is produced can be used to run electricial appliances like washing machines, 
hairdryers and lighting.

<h2> How Do Solar PV Systems Work? </h2>

Solar Photovoltaic cells are installed on the roof or walls of a household residence. 
When the sun shines, the photovoltaic cells generated direct current(DC) 
which then travels to an inverter to be converted into alternating current(AC).
The AC is used to provide electricity to household appliances. 
The two common types of systems are 'Grid-Connected' and 'Off-Grid'.
</br></br>
<h3> Grid-Connected </h3>

Grid-connected systems are the most common type of system because they are 
simpler to design and easier to install than off-grid systems.
They are connected to the electriciy grid. Most people prefer this 
type of system because of the advantages that these systems offer by 
the Government like the Feed-In Tariff incentive. Electricity produced 
can be either used, exported or imported to and from the national grid
so the household has sufficient amount of electricity at all times.
</br>
For more information on Feed In Tariff please click the here. 
