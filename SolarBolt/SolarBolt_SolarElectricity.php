</br></br>
<h3> Off-Grid </h3>
The off-grid system can also be known as the stand alone system. 
It generates electricty like any PV system but is stored independently from the 
national grid. These systems are usually found in remote locations. 
The electricity can be stored in batteries but in DC and then need to be converted
into AC before use. These batteries are usually very expensive to 
buy and maintain due to special requirements in storing electricity for longer periods.

