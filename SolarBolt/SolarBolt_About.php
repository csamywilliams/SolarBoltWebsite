<h2> About Solar Bolt </h2>
<img src="htdocs/images/amyj.jpg" style="width: 50%;" style="height: 50%;"></br></br>
Solar Bolt is created by Amy Jenkins, a Swansea University student. It is her final
year project as part of the BSc in Computer Science. </br>
The aim of the project is to provide a useful and incentive way for users to monitor their PV Output. </br>
The project is implemented using PHP, MySQL, JavaScript, HighCharts[1], JQuery[2], HTML and CSS. 
</br></br>
<b> Features included </b></br>
<ul>
<li>Dynamic visualisations using HighCharts</li>
<li>Various views including Daily, Weekly, Monthly and Yearly</li>
<li>Shows Feed in tariff savings </li>
<li>Information on Solar Electricity </li>
<li>Solar Energy Calculator tool </li>
<li>Personalized profile </li>
<li>Forum: online community </li>

and much more... 
</ul>

